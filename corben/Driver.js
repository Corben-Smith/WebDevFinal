class Driver{
    constructor(name, carNumber, age, team, home, championship, wins, desc){
        this.name = name;
        this.carNumber = carNumber;
        this.age = age;
        this.team = team;
        this.home = home;
        this.championship = championship;
        this.wins = wins;
        this.desc = desc;
    }
}

module.exports = Driver;
